    def computeActionFromValues(self, state):
        """
          The policy is the best action in the given state
          according to the values currently stored in self.values.

          You may break ties any way you see fit.  Note that if
          there are no legal actions, which is the case at the
          terminal state, you should return None.
        """
        "*** YOUR CODE HERE ***"
        
        #Return none if at the terminal state
        if self.mdp.isTerminal(state):
            return None

        #Get all possible actions, initialize the first to be the best action
        possActions = self.mdp.getPossibleActions(state)
        if len(possActions) == 0:
            return None
        bestAction = possActions[0]
        maxQValue = self.getQValue(state, bestAction)

        #Look through all possible actions, setting the action with the highest QValue to be the best
        for action in possActions:
            if self.getQValue(state, action) > maxQValue:
                maxQValue = self.getQValue(state, action)
                bestAction = action
        return bestAction
        
